package com.thoughtworks.trackmanagement.model;

public class Talk {
	
	private String title;
	private int time;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
}
